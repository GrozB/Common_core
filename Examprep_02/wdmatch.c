#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	if (argc == 3)
	{
		while (argv[1][i])
		{
			while (argv[2][j] != '\0' && argv[2][j] != argv[1][i])
				j++;
			if (argv[2][j] == '\0')
			{
				write(1, "\n", 1);
				return (0);
			}
			i++;
			j++;
		}
		write(1, argv[1], i);
	}
	write(1, "\n", 1);
	return (0);
}