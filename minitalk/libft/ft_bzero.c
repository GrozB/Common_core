#include <stdio.h>

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = (unsigned char *)s;
	while (n != 0)
	{
		str[i] = '\0';
		n--;
		i++;
	}
}
